    fn one_schema_with(schema: Schema) -> Catalog {
        Catalog {
            schemas: vec![schema],
        }
    }

    /// Build a split `Output` from `(kind, file)` pairs via the shared parser.
    fn split(pairs: &[(&str, &str)]) -> Output {
        let body = pairs
            .iter()
            .map(|(kind, file)| format!("{kind} = \"{file}\""))
            .collect::<Vec<_>>()
            .join("\n");
        toml::from_str(&body).expect("valid output split")
    }

    /// Output split so tables, enums, composites and domains each land in their own file.
    fn split_by_kind() -> Output {
        split(&[
            ("tables", "tables.ts"),
            ("enums", "enums.ts"),
            ("composites", "composites.ts"),
            ("domains", "domains.ts"),
        ])
    }

    fn file<'a>(output: &'a EmitterOutput, name: &str) -> &'a GeneratedFile {
        output
            .files
            .iter()
            .find(|file| file.path.to_str() == Some(name))
            .unwrap_or_else(|| panic!("expected a file named {name}"))
    }

