const HEADER: &str = "// Generated by gieter. Do not edit manually.\n\n";
const EMITTER: &str = "typescript";

struct Context<'a> {
    options: &'a Options,
    current_file: &'a str,
    brands: &'a Brands,
}

pub fn render(catalog: &Catalog, options: &Options) -> Result<EmitterOutput, EmitError> {
    let brands = Brands::resolve(catalog, &options.brand);
    let output = &options.output;
    let mut files = Files::new(EMITTER, format_import);

    for (kind, declaration) in declarations(catalog, &brands) {
        files.emit(output, kind, |file| {
            let mut imports = vec![];
            let context = Context {
                options,
                current_file: file,
                brands: &brands,
            };

            let code = match declaration {
                Declaration::Brand(name, scalar) => {
                    render_brand(&context, name, scalar, &mut imports)
                }
                Declaration::Enum(enum_definition) => render_enum(&context, enum_definition),
                Declaration::Composite(composite) => render_object(
                    &context,
                    &composite.name.to_case(Case::Pascal),
                    &composite.fields,
                    &None,
                    &mut imports,
                    None,
                ),
                Declaration::Domain(domain) => render_domain(&context, domain, &mut imports),
                Declaration::Table(table) => render_object(
                    &context,
                    &table.name.to_case(Case::Pascal),
                    &table.columns,
                    &table.comment,
                    &mut imports,
                    Some(table),
                ),
                Declaration::View(view) => render_object(
                    &context,
                    &view.name.to_case(Case::Pascal),
                    &view.columns,
                    &view.comment,
                    &mut imports,
                    None,
                ),
            };

            Fragment { code, imports }
        })?;
    }

    Ok(files.finish(HEADER))
}

fn render_object(
    context: &Context,
    name: &str,
    columns: &[Column],
    comment: &Option<String>,
    imports: &mut Vec<Import>,
    table: Option<&Table>,
) -> String {
    let mut writer = Writer::with_indent(&context.options.indent);

    if context.options.comments
        && let Some(text) = comment
    {
        write_doc(&mut writer, std::slice::from_ref(text));
    }

    let (open, close) = match context.options.type_style {
        TypeStyle::Interface => (format!("export interface {name} {{"), "}"),
        TypeStyle::Type => (format!("export type {name} = {{"), "};"),
    };

    writer.block(&open, close, |writer| {
        for column in columns {
            render_column(context, column, imports, writer, table);
        }
    });

    writer.finish()
}

fn render_column(
    context: &Context,
    column: &Column,
    imports: &mut Vec<Import>,
    writer: &mut Writer,
    table: Option<&Table>,
) {
    let mut doc = vec![];

    if context.options.comments
        && let Some(text) = &column.comment
    {
        doc.push(text.clone());
    }

    if context.options.key_comments
        && let Some(table) = table
    {
        if table.primary_key.iter().any(|pk| pk == &column.name) {
            doc.push("PK".to_string());
        }

        if let Some(reference) = fk_reference(&table.foreign_keys, &column.name) {
            doc.push(format!("FK {reference}"));
        }
    }

    write_doc(writer, &doc);

    let ty = match brand_ty(context, column, table, imports) {
        Some(branded) => branded,
        None => column_ts(context, &column.ty, imports),
    };

    let name = property_name(&column.name, &context.options.property_case);

    let line = match context.options.null_style {
        NullStyle::Union if column.nullable => format!("{name}: {ty} | null;"),
        NullStyle::Optional if column.nullable => format!("{name}?: {ty};"),
        _ => format!("{name}: {ty};"),
    };

    writer.line(&line);
}

/// A `references <schema>.<table>.<column>` note for a column that is part of a foreign
/// key. Columns map to `ref_columns` positionally, so this works for composite keys too.
fn fk_reference(foreign_keys: &[ForeignKey], column: &str) -> Option<String> {
    for foreign_key in foreign_keys {
        if let Some(index) = foreign_key.columns.iter().position(|c| c == column)
            && let Some(ref_column) = foreign_key.ref_columns.get(index)
        {
            let (schema, table) = &foreign_key.ref_table;
            return Some(format!("references {schema}.{table}.{ref_column}"));
        }
    }
    None
}

/// Emit a JSDoc comment: nothing for no lines, a one-liner for a single line, a block otherwise.
fn write_doc(writer: &mut Writer, lines: &[String]) {
    match lines {
        [] => {}
        [single] => writer.line(&format!("/** {single} */")),
        many => {
            writer.line("/**");
            for line in many {
                writer.line(&format!(" * {line}"));
            }
            writer.line(" */");
        }
    }
}

/// The brand type for a column, if it carries one, importing it when the `brands` declarations
/// live in a different file than this column.
fn brand_ty(
    context: &Context,
    column: &Column,
    table: Option<&Table>,
    imports: &mut Vec<Import>,
) -> Option<String> {
    let table = table?;

    let name = context
        .brands
        .brand_of(&table.schema, &table.name, &column.name)?;

    if let Some(target) = import_target(&context.options.output, context.current_file, Kind::Brands)
    {
        imports.push(Import::Named {
            target: target.to_string(),
            symbol: name.to_string(),
        });
    }

    Some(name.to_string())
}

fn format_import(module: &str, symbols: &[&str]) -> String {
    format!(
        "import type {{ {} }} from '{}';",
        symbols.join(", "),
        module_specifier(module)
    )
}

fn module_specifier(file: &str) -> String {
    format!("./{}", file.strip_suffix(".ts").unwrap_or(file))
}

fn column_ts(context: &Context, ty: &ColumnType, imports: &mut Vec<Import>) -> String {
    match ty {
        ColumnType::Scalar(scalar) => {
            let mapped = typemap::resolve(scalar, &context.options.types);
            if let Some(import) = mapped.import {
                imports.push(Import::Raw(import));
            }
            mapped.ts
        }
        ColumnType::Array(inner) => {
            format!("{}[]", column_ts(context, inner, imports))
        }
        ColumnType::Enum { name, .. } => named_type(context, name, Kind::Enums, imports),
        ColumnType::Composite { name, .. } => named_type(context, name, Kind::Composites, imports),
        ColumnType::Domain { name, .. } => named_type(context, name, Kind::Domains, imports),
    }
}

fn named_type(context: &Context, name: &str, kind: Kind, imports: &mut Vec<Import>) -> String {
    let name = name.to_case(Case::Pascal);
    if let Some(target) = import_target(&context.options.output, context.current_file, kind) {
        imports.push(Import::Named {
            target: target.to_string(),
            symbol: name.clone(),
        });
    }
    name
}

fn brand_type(name: &str, base_ts: &str) -> String {
    let mut writer = Writer::new();
    writer.line(&format!(
        "export type {name} = {base_ts} & {{ readonly __brand: '{name}' }};"
    ));
    writer.finish()
}

fn render_brand(
    context: &Context,
    name: &str,
    scalar: &ScalarType,
    imports: &mut Vec<Import>,
) -> String {
    let mapped = typemap::resolve(scalar, &context.options.types);
    if let Some(import) = mapped.import {
        imports.push(Import::Raw(import));
    }
    brand_type(name, &mapped.ts)
}

fn render_domain(context: &Context, domain: &Domain, imports: &mut Vec<Import>) -> String {
    let base_ts = column_ts(context, &domain.base, imports);
    let name = domain.name.to_case(Case::Pascal);
    brand_type(&name, &base_ts)
}

fn render_enum(context: &Context, enum_definition: &Enum) -> String {
    let name = enum_definition.name.to_case(Case::Pascal);

    let mut writer = Writer::with_indent(&context.options.indent);

    match context.options.enum_style {
        EnumStyle::Union => {
            let members: Vec<String> = enum_definition
                .values
                .iter()
                .map(|value| format!("\"{value}\""))
                .collect();
            writer.line(&format!("export type {name} = {};", members.join(" | ")));
        }
        EnumStyle::Enum => {
            writer.block(&format!("export enum {name} {{"), "}", |writer| {
                let mut used: BTreeSet<String> = BTreeSet::new();
                for value in &enum_definition.values {
                    let base = enum_member(value);
                    let mut member = base.clone();
                    let mut suffix = 2;
                    while used.contains(&member) {
                        member = format!("{base}{suffix}");
                        suffix += 1;
                    }
                    used.insert(member.clone());
                    writer.line(&format!("{member} = \"{value}\","));
                }
            });
        }
        EnumStyle::Const => {
            writer.block(
                &format!("export const {name} = {{"),
                "} as const;",
                |writer| {
                    for value in &enum_definition.values {
                        writer.line(&format!("{}: \"{value}\",", object_key(value)));
                    }
                },
            );
            writer.line(&format!(
                "export type {name} = (typeof {name})[keyof typeof {name}];"
            ));
        }
    }

    writer.finish()
}

